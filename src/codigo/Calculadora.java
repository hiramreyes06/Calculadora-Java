    private char getSigno(){
        
        char signo='+';
     for(int a=0; a<jLabel1.getText().length()-1; a++){
     
       if( jLabel1.getText().charAt(a) =='+' ||
       jLabel1.getText().charAt(a) =='-' ||
       jLabel1.getText().charAt(a) =='*' ||
       jLabel1.getText().charAt(a) =='/'        ){
       signo=jLabel1.getText().charAt(a);
       
       }
         
     }  
     return signo;
    
    }
    
    private boolean esSigno(){
    
       if( jLabel1.getText().charAt( jLabel1.getText().length()-1) == '+' ||
         jLabel1.getText().charAt( jLabel1.getText().length()-1) == '-' ||
         jLabel1.getText().charAt( jLabel1.getText().length()-1) == '*' ||
          jLabel1.getText().charAt( jLabel1.getText().length()-1) == '/'      ){
       
       return true;
       } 
      
       else{
       
       return false;
       }
    }
    
    
