    private void jButton30ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton30ActionPerformed
      //BTN =
      String[] operando= new String[2];
    
      
      try{
    
          
          switch( getSigno() ){
              
              case '+':
               operando= jLabel1.getText().split("\\+");
        
                jLabel1.setText( ( Integer.parseInt(operando[0]) + Integer.parseInt(operando[1]) ) +"" );  
                  
              break;
              case '-':
               operando= jLabel1.getText().split("\\-");
        
                jLabel1.setText( ( Integer.parseInt(operando[0]) - Integer.parseInt(operando[1]) ) +"" );    
                  
              break;
              case '*':
                  
              operando= jLabel1.getText().split("\\*");
        
                jLabel1.setText( ( Integer.parseInt(operando[0]) * Integer.parseInt(operando[1]) ) +"" );     
                  
              break;
              case '/':
                  
              operando= jLabel1.getText().split("\\/");
        
              jLabel1.setText( ( Integer.parseInt(operando[0]) / Integer.parseInt(operando[1]) ) +"" );     
                  
              break;    
              default:
          
          }
      
      
          
    
      
      }catch(Exception ex){
      jLabel1.setText("ERROR");
          System.out.println("Error: "+ ex);
          
          
      }
      
      

      
      
    }//GEN-LAST:event_jButton30ActionPerformed

